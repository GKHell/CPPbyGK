
#include<iostream>
using namespace std;
int main()
{
	int a = 5,b = 3;
	int c;
	/****************** Bitwise AND
	 *	  8 4 2 1
	 * -----------
	 * 5: 0 1 0 1
	 * 3: 0 0 1 1
	 * ----------
	 * &: 0 0 0 1 =>  1
	 * 
	 * ***************/

	c = a&b;
	cout<<a<<" & "<<b<<" = "<<c<<endl;

	/****************** Bitwise OR
	 *	  8 4 2 1
	 * -----------
	 * 5: 0 1 0 1
	 * 3: 0 0 1 1
	 * ----------
	 * |: 0 1 1 1 => 7
	 * 
	 * ***************/

	c = a|b;
	cout<<a<<" | "<<b<<" = "<<c<<endl;

	/****************** Bitwise Ex-OR : ^
	 *	  8 4 2 1
	 * -----------
	 * 5: 0 1 0 1
	 * 3: 0 0 1 1
	 * ----------
	 * ^: 0 1 1 0 => 6
	 * 
	 * ***************/

	c = a^b;
	cout<<a<<" ^ "<<b<<" = "<<c<<endl;

	/****************** Bitwise Negation
	*
	* c = ~a
	*   = -(a+1)
	*   = -(5+1)
	*   = -6
	* 
	 * ***************/

	c = ~a;
	cout<<" ~ "<<a<<" = "<<c<<endl;

	/****************** Bitwise Left Shift
	 * a=5,b=3
	 * 5 : 0000 0101
	 * 	 : 0000 0101 xxx
	 * 	 : 0010 1xxx
	 * 	 : 0010 1000 => 40
	 * -------------------
	 * c = a*2^b
	 *   = 5*2^3
	 *   = 5*8
	 *   = 40
	 * ***************/

	c = a<<b;
	cout<<a<<" << "<<b<<" = "<<c<<endl;

	/****************** Bitwise RIght Shift
	 * a=5,b=3
	 * 5 : 0000 0101
	 * 	 : xxx 0000 0101
	 * 	 : xxx0 0000
	 * 	 : 0 =>0
	 * -------------------
	 * c = a/2^b
	 *   = 5/2^3
	 *   = 5/8
	 *   = 0.x
	 * ***************/

	c = a>>b;
	cout<<a<<" >> "<<b<<" = "<<c<<endl;

}
